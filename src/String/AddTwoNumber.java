package String;

public class AddTwoNumber {
    public static void main(String[] args) {
        String num1 = "110";
        String num2 = "10";
        int a = Integer.parseInt(num1);
        int b = Integer.parseInt(num2);
        int ans = a + b;


        String ans1 = Integer.toString(ans);
        System.out.println(ans1);
    }
}
